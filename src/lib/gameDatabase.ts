/**
 * Get user's games
 */
export const getUserGames = async (
  userId: string,
  filters?: GameFilters
): Promise<{ success: boolean; data?: Game[]; error?: string }> => {
  try {
    let query = defaultSupabase
      .from('games')
      .select('*')
      .eq('user_id', userId)
      .eq('is_active', true);

    // Apply filters
    if (filters?.game_type) {
      query = query.eq('game_type', filters.game_type);
    }
    if (filters?.difficulty) {
      query = query.eq('difficulty', filters.difficulty);
    }
    if (filters?.subject) {
      query = query.eq('subject', filters.subject);
    }

    // Sorting
    if (filters?.sort_by === 'recent') {
      query = query.order('created_at', { ascending: false });
    } else if (filters?.sort_by === 'popular') {
      query = query.order('total_plays', { ascending: false });
    } else if (filters?.sort_by === 'rating') {
      query = query.order('avg_score', { ascending: false });
    } else {
      query = query.order('created_at', { ascending: false });
    }

    // Pagination
    if (filters?.limit) {
      query = query.limit(filters.limit);
    }
    if (filters?.offset) {
      query = query.range(filters.offset, filters.offset + (filters.limit || 10) - 1);
    }

    const { data, error } = await query;

    if (error) {
      console.error('Error fetching user games:', error);
      return { success: false, error: getErrorMessage(error) };
    }

    return { success: true, data: data || [] };
  } catch (error) {
    console.error('Unexpected error fetching user games:', error);
    return { success: false, error: getErrorMessage(error) };
  }
};

