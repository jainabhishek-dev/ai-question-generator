/**
 * Get leaderboard for a game
 */
export const getGameLeaderboard = async (
  query: LeaderboardQuery
): Promise<{ success: boolean; data?: LeaderboardEntry[]; error?: string }> => {
  try {
    let dbQuery = defaultSupabase
      .from('game_leaderboards')
      .select(`
        *,
        users:user_id (email)
      `)
      .eq('game_id', query.game_id)
      .order('best_score', { ascending: false });

    // Pagination
    if (query.limit) {
      dbQuery = dbQuery.limit(query.limit);
    }
    if (query.offset) {
      dbQuery = dbQuery.range(query.offset, query.offset + (query.limit || 10) - 1);
    }

    const { data, error } = await dbQuery;

    if (error) {
      console.error('Error fetching leaderboard:', error);
      return { success: false, error: getErrorMessage(error) };
    }

    // Transform data to LeaderboardEntry format
    const entries: LeaderboardEntry[] = (data || []).map((entry, index) => ({
      rank: (query.offset || 0) + index + 1,
      user_id: entry.user_id,
      user_email: (entry.users as { email: string })?.email || 'Anonymous',
      best_score: entry.best_score,
      best_time_seconds: entry.best_time_seconds,
      best_accuracy: entry.best_accuracy,
      total_plays: entry.total_plays,
      perfect_score_count: entry.perfect_score_count,
      percentile: entry.percentile
    }));

    return { success: true, data: entries };
  } catch (error) {
    console.error('Unexpected error fetching leaderboard:', error);
    return { success: false, error: getErrorMessage(error) };
  }
};

