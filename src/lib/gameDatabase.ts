/**
 * Check if user liked a game
 */
export const checkGameLike = async (
  gameId: string,
  userId: string
): Promise<{ success: boolean; liked: boolean; error?: string }> => {
  try {
    const { data, error } = await defaultSupabase
      .from('game_likes')
      .select('game_id')
      .eq('game_id', gameId)
      .eq('user_id', userId)
      .single();

    if (error) {
      // Not liked if not found
      if (error.code === 'PGRST116') {
        return { success: true, liked: false };
      }
      console.error('Error checking game like:', error);
      return { success: false, liked: false, error: getErrorMessage(error) };
    }

    return { success: true, liked: !!data };
  } catch (error) {
    console.error('Unexpected error checking game like:', error);
    return { success: false, liked: false, error: getErrorMessage(error) };
  }
};
