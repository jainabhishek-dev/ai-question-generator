/**
 * Create a new game
 */
export const createGame = async (
  gameData: CreateGameRequest,
  userId: string,
  supabaseClient?: SupabaseClient
): Promise<{ success: boolean; data?: Game; error?: string }> => {
  try {
    const supabase = supabaseClient || defaultSupabase;
    const { data, error } = await supabase
      .from('games')
      .insert({
        title: gameData.title,
        description: gameData.description || null,
        topic: gameData.topic,
        game_type: gameData.game_type,
        difficulty: gameData.difficulty,
        subject: gameData.subject || null,
        grade_level: gameData.grade_level || null,
        config: gameData.config,
        thumbnail_url: gameData.thumbnail_url || null,
        user_id: userId,
        is_public: gameData.is_public ?? true,
        allow_anonymous_play: gameData.allow_anonymous_play ?? true,
        share_code: '' // Will be auto-generated by trigger
      })
      .select()
      .single();

    if (error) {
      console.error('Error creating game:', error);
      return { success: false, error: getErrorMessage(error) };
    }

    // Add tags if provided
    if (gameData.tags && gameData.tags.length > 0) {
      const tagInserts = gameData.tags.map(tag => ({
        game_id: data.id,
        tag: tag,
        tag_type: 'topic'
      }));

      const { error: tagError } = await supabase
        .from('game_tags')
        .insert(tagInserts);

      if (tagError) {
        console.error('Error adding tags:', tagError);
        // Don't fail the whole operation for tag errors
      }
    }

    return { success: true, data };
  } catch (error) {
    console.error('Unexpected error creating game:', error);
    return { success: false, error: getErrorMessage(error) };
  }
};

