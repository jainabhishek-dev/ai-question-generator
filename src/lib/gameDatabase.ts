/**
 * Discover public games with filters
 */
export const discoverGames = async (
  filters?: GameFilters
): Promise<{ success: boolean; data?: GameWithCreator[]; error?: string }> => {
  try {
    // Use the helper view for public games
    let query = defaultSupabase
      .from('public_games_feed')
      .select('*');

    // Apply filters
    if (filters?.game_type) {
      query = query.eq('game_type', filters.game_type);
    }
    if (filters?.difficulty) {
      query = query.eq('difficulty', filters.difficulty);
    }
    if (filters?.subject) {
      query = query.eq('subject', filters.subject);
    }
    if (filters?.topic) {
      query = query.ilike('topic', `%${filters.topic}%`);
    }
    if (filters?.min_plays) {
      query = query.gte('total_plays', filters.min_plays);
    }
    if (filters?.is_featured) {
      query = query.eq('is_featured', true);
    }

    // Sorting
    if (filters?.sort_by === 'recent') {
      query = query.order('created_at', { ascending: false });
    } else if (filters?.sort_by === 'popular') {
      query = query.order('total_plays', { ascending: false });
    } else if (filters?.sort_by === 'rating') {
      query = query.order('avg_score', { ascending: false });
    } else {
      query = query.order('created_at', { ascending: false });
    }

    // Pagination
    if (filters?.limit) {
      query = query.limit(filters.limit);
    }
    if (filters?.offset) {
      query = query.range(filters.offset, filters.offset + (filters.limit || 20) - 1);
    }

    const { data, error } = await query;

    if (error) {
      console.error('Error discovering games:', error);
      return { success: false, error: getErrorMessage(error) };
    }

    return { success: true, data: data || [] };
  } catch (error) {
    console.error('Unexpected error discovering games:', error);
    return { success: false, error: getErrorMessage(error) };
  }
};

